"""
Задание 5: Проверка пароля
"""
password = input("Введите пароль: ")
if not password:
    print("Пароль не может быть пустым")
elif len(password) < 6:
    print("Пароль слишком короткий (минимум 6 символов)")
elif len(password) > 14:
    print("Пароль слишком длинный (максимум 14 символов)")
else:
    print("Пароль корректный")